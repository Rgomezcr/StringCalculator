namespace StringCalculatorKata
{
    public class StringCalculator
    {
        public int Add(string number)
        {
            if (number == "1")
                return 1;
            if (number == "2")
                return 2;
            if (number == "3")
                return 3;
            if (number == "4")
                return 4;
            if (number == "5")
                return 5;
            return 0;
        }
    }
}